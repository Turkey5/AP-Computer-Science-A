/* Lesson 1 Coding Activity Question 2 */

class U1_L1_Activity_Two {
  public static void main(String[] args) {
    System.out.println(" Coding is fun! ");
    System.out.println(" Coding is fun! ");
    System.out.println(" Coding is fun! ");

    // by using println() instead of print(), each statement will start a new line after being printed
    // if we only used the print() method, the output would look like this:
    // " Coding is fun!  Coding is fun!  Coding is fun! "
    // note how there are no new lines being created
  }
}
